import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const projectRoot = path.resolve(scriptDir, '..')
const sourcePath = path.join(projectRoot, 'vendor', 'src-brain-upstream', 'queryEngineRunnerAdapter.js')
const distDir = path.join(projectRoot, 'dist')
const distPackageJsonPath = path.join(distDir, 'package.json')
const distQueryEnginePath = path.join(distDir, 'QueryEngine.js')

function loadUpstreamSource() {
  if (!fs.existsSync(sourcePath)) {
    throw new Error(`Fonte upstream nao encontrada: ${sourcePath}`)
  }

  const source = fs.readFileSync(sourcePath, 'utf8')
  if (!source.includes('export async function runQueryEngine')) {
    throw new Error('A fonte upstream nao contem a exportacao esperada de runQueryEngine.')
  }
  return source
}

function buildPackagedModule(source) {
  const upstreamSource = source.replace(
    'export async function runQueryEngine(',
    'async function runPackagedUpstreamQueryEngine(',
  )

  const banner = [
    '// Generated by scripts/package-queryengine.mjs',
    '// Source: vendor/src-brain-upstream/queryEngineRunnerAdapter.js',
    '// This file packages the selected upstream QueryEngine subset and',
    '// falls back to the active authority for execution-heavy requests.',
    '',
  ].join('\n')

  const wrapper = `
import fusionBrainModule from '../core/brain/fusionBrain.js'

const activeRunQueryEngine =
  typeof fusionBrainModule?.runQueryEngine === 'function'
    ? fusionBrainModule.runQueryEngine
    : typeof fusionBrainModule?.default?.runQueryEngine === 'function'
      ? fusionBrainModule.default.runQueryEngine
      : null

function normalizeRoutingText(value) {
  return String(value || '')
    .normalize('NFD')
    .replace(/[\\u0300-\\u036f]/g, '')
    .toLowerCase()
    .trim()
}

function hasAnySessionKey(session, keys) {
  return keys.some(key => Object.prototype.hasOwnProperty.call(session || {}, key))
}

function shouldUseActiveAuthority(payload) {
  const session = payload?.session || {}
  if (
    hasAnySessionKey(session, [
      'executor_bridge',
      'runtime_mode',
      'milestone_plan',
      'repository_analysis',
      'task_id',
      'run_id',
      'checkpoint_id',
      'resume_from',
    ])
  ) {
    return true
  }

  const message = normalizeRoutingText(payload?.message)
  if (!message) {
    return false
  }

  const engineeringSignals = [
    'package.json',
    'cargo',
    'npm',
    'repositorio',
    'repository',
    'arquivo',
    'file',
    'teste',
    'tests',
    'corrija',
    'corrigir',
    'fix',
    'bug',
    'erro',
    'error',
    'codigo',
    'code',
    'commit',
    'branch',
    'worktree',
    'leia ',
    'analyze the repository',
    'analise o repositorio',
  ]

  return engineeringSignals.some(signal => message.includes(signal))
}

export async function runQueryEngine(payload) {
  if (shouldUseActiveAuthority(payload) && typeof activeRunQueryEngine === 'function') {
    return activeRunQueryEngine(payload)
  }

  return runPackagedUpstreamQueryEngine(payload)
}

export { runPackagedUpstreamQueryEngine }
export default runQueryEngine
`.trim()

  return `${banner}${upstreamSource.trim()}\n\n${wrapper}\n`
}

export function packageQueryEngine() {
  const source = loadUpstreamSource()
  fs.mkdirSync(distDir, { recursive: true })
  fs.writeFileSync(
    distPackageJsonPath,
    `${JSON.stringify({ type: 'module' }, null, 2)}\n`,
    'utf8',
  )
  fs.writeFileSync(distQueryEnginePath, buildPackagedModule(source), 'utf8')

  return {
    ok: true,
    source: sourcePath,
    output: distQueryEnginePath,
    package_json: distPackageJsonPath,
  }
}

const executedAsEntrypoint =
  process.argv[1] &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)

if (executedAsEntrypoint) {
  console.log(JSON.stringify(packageQueryEngine(), null, 2))
}
